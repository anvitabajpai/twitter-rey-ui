export function toIntMonth(abbr) {
  return monthMap[abbr];
}

export function toAbbrMonth(intMth) {
  return Object.keys(monthMap).find(
    m => monthMap[m] === intMth.toString('02d'),
  );
}

export function toFullState(abbr) {
  return stateMap[abbr] || '';
}

export function toAbbrState(stateName) {
  return Object.keys(stateMap).find(m => stateMap[m] === stateName) || '';
}
